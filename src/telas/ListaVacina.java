    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        try {
            String pesquisa = txtPesquisar.getText().toLowerCase();

            VacinaDAO dao = new VacinaDAO();
            DefaultTableModel modelo = (DefaultTableModel) tblVacina.getModel();
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");

            modelo.setRowCount(0); // limpa tabela

            List<Vacina> lista = dao.listar();

            for (Vacina v : lista) {

                String nomeAnimal = "";

                if (v.getAnimal() != null) {
                    nomeAnimal = v.getAnimal().getNome().toLowerCase();
                }

                // ? FILTRO PELO NOME DO ANIMAL
                if (pesquisa.isEmpty() || nomeAnimal.contains(pesquisa)) {

                    modelo.addRow(new Object[]{
                        v.getIdVacina(),
                        v.getVacinaNome(),
                        v.getVacinaMarca(),
                        v.getLote(),
                        v.getVacinaAplicacao() != null
                        ? v.getVacinaAplicacao().format(formatter)
                        : "",
                        v.getVacinaValidade() != null
                        ? v.getVacinaValidade().format(formatter)
                        : "",
                        v.getMicrochip(),
                        v.getMicrochipAplicacao() != null
                        ? v.getMicrochipAplicacao().format(formatter)
                        : "",
                        v.getAnimal() != null
                        ? v.getAnimal().getNome()
                        : "Sem animal"
                    });
                }
            }

            if (modelo.getRowCount() == 0) {
                JOptionPane.showMessageDialog(null, "Nenhuma vacina encontrada!");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao pesquisar: " + e.getMessage());
        }

    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        try {
            int linha = tblVacina.getSelectedRow();

            if (linha == -1) {
                JOptionPane.showMessageDialog(null, "Selecione uma vacina!");
                return;
            }

            int idVacina = (int) tblVacina.getValueAt(linha, 0);
            Vacina vacina = new VacinaDAO().buscarPorId(idVacina);

            // Confirmaçăo
            int confirm = JOptionPane.showConfirmDialog(
                    null,
                    "Tem certeza que deseja excluir?",
                    "Confirmaçăo",
                    JOptionPane.YES_NO_OPTION
            );

            if (confirm != JOptionPane.YES_OPTION) {
                return;
            }

            // Excluir
            VacinaDAO dao = new VacinaDAO();
            dao.deletar(idVacina);

            JOptionPane.showMessageDialog(null, "Vacina excluída com sucesso!");

            // ? Atualizar tabela
            ((DefaultTableModel) tblVacina.getModel()).removeRow(linha);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao excluir: " + e.getMessage());
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        try {
            int linha = tblVacina.getSelectedRow();

            if (linha == -1) {
                JOptionPane.showMessageDialog(null, "Selecione uma vacina!");
                return;
            }

            int idVacina = (int) tblVacina.getValueAt(linha, 0);

            VacinaDAO dao = new VacinaDAO();
            Vacina v = dao.buscarPorId(idVacina);

            if (v == null) {
                JOptionPane.showMessageDialog(null, "Vacina năo encontrada!");
                return;
            }

            // Abre tela de ediçăo
            TelaVacina tela = new TelaVacina();
            tela.setVacina(v); // ? AQUI É O SEGREDO

            tela.setVisible(true);
            this.dispose();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao abrir ediçăo: " + e.getMessage());
        }

    }//GEN-LAST:event_btnAtualizarActionPerformed

