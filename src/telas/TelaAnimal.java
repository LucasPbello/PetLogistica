    private void carregarCombos() {

        try {
            ClienteDAO clienteDAO = new ClienteDAO();
            OrigemDestinoDAO odDAO = new OrigemDestinoDAO();

            // limpa antes
            cmbCliente.removeAllItems();
            cmbOrigem.removeAllItems();

            // ? CLIENTES
            listaClientes = clienteDAO.listar();

            for (Cliente c : listaClientes) {
                cmbCliente.addItem(c.getNome());
            }

            // ? ORIGEM DESTINO
            listaOrigem = odDAO.listar();

            for (OrigemDestino od : listaOrigem) {
                cmbOrigem.addItem(
                        od.getPaisOrigem() + " ? " + od.getPaisDestino()
                );
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao carregar combos: " + e.getMessage());
        }
    }

